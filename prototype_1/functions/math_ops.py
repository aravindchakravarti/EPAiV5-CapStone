from typing import Union

def ai_add_two_numbers(a: Union[int, float], b: Union[int, float]) -> Union[int, float]:
    """
    Adds two numbers and returns the result.

    Args:
        a (Union[int, float]): First number.
        b (Union[int, float]): Second number.

    Returns:
        Union[int, float]: Sum of the two numbers.
    """
    return a + b

def ai_sub_two_numbers(a: Union[int, float], b: Union[int, float]) -> Union[int, float]:
    """
    Subtracts two numbers and returns the result.

    Args:
        a (Union[int, float]): First number.
        b (Union[int, float]): Second number.
    """
    return a - b

def ai_mul_two_numbers(a: Union[int, float], b: Union[int, float]) -> Union[int, float]:
    """
    Multiplies two numbers and returns the result.

    Args:
        a (Union[int, float]): First number.
        b (Union[int, float]): Second number.

    Returns:
        Union[int, float]: Product of the two numbers.
    """
    return a * b